func CreateBatch_projects_locations_state_reportTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("post_v1_parent_state_report",
		mcp.WithDescription("Report agent's state, e.g. agent status and tasks information"),
		mcp.WithString("parent", mcp.Required(), mcp.Description("Required. Format: projects/{project}/locations/{location} {project} should be a project number.")),
		mcp.WithObject("metadata", mcp.Description("Input parameter: VM Agent Metadata.")),
		mcp.WithObject("agentInfo", mcp.Description("Input parameter: VM Agent Info.")),
		mcp.WithObject("agentTimingInfo", mcp.Description("Input parameter: VM timing information")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    Batch_projects_locations_state_reportHandler(cfg),
	}
}
